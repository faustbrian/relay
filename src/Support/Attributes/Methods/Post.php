namespace Cline\Relay\Support\Attributes\Methods;

use Attribute;

/**
 * Mark a request as using the HTTP POST method.
 *
 * @author Brian Faust <brian@cline.sh>
 *
 * @psalm-immutable
 */
#[Attribute(Attribute::TARGET_CLASS)]
final readonly class Post implements HttpMethod
{
    public function method(): string
    {
        return 'POST';
    }
}
